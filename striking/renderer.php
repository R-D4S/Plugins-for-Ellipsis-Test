class qtype_striking_renderer extends qtype_renderer {
    public function formulation_and_controls(question_attempt $qa,
            question_display_options $options) {

        $question = $qa->get_question();
        $currentanswer = $qa->get_last_qt_var('answer');

        $inputname = $qa->get_qt_field_name('answer');
        $inputattributes = array(
            'type' => 'text',
            'name' => $inputname,
            'value' => $currentanswer,
            'id' => $inputname,
            'size' => 80,
            'class' => 'form-control d-inline',
        );

        if ($options->readonly) {
            $inputattributes['readonly'] = 'readonly';
        }

        /*$feedbackimg = '';
        if ($options->correctness) {
            $answer = $question->get_matching_answer(array('answer' => $currentanswer));
            if ($answer) {
                $fraction = $answer->fraction;
            } else {
                $fraction = 0;
            }
            $inputattributes['class'] .= ' ' . $this->feedback_class($fraction);
            $feedbackimg = $this->feedback_image($fraction);
        }
        */

        $questiontext = $question->format_questiontext($qa);

        $sentence = $question->sentence;
        $words=explode(" ", $sentence);

        $placeholder = false;
        if (preg_match('/_____+/', $questiontext, $matches)) {
            $placeholder = $matches[0];
            $inputattributes['size'] = round(strlen($placeholder) * 1.1);
        }
        $input = html_writer::empty_tag('input', $inputattributes);

        if ($placeholder) {
            $inputinplace = html_writer::tag('label', get_string('answer'),
                    array('for' => $inputattributes['id'], 'class' => 'accesshide'));
            $inputinplace .= $input;
            $questiontext = substr_replace($questiontext, $inputinplace,
                    strpos($questiontext, $placeholder), strlen($placeholder));
        }

        //деление предложения на элементы

        $result = html_writer::tag('div', $questiontext, array('class' => 'qtext'));
        $result .= html_writer::start_tag('div', array('class' => 'qsentence'));
        $i=0;
        foreach ($words as $w){
            $result .=html_writer::tag('span', $w, array('class' => 'striking_elem', 'id' => strval($i)));
            $result .=" ";
            $i+=1;
        }
        $result .= html_writer::end_tag('div');

        if (!$placeholder) {
            $result .= html_writer::start_tag('div', array('class' => 'ablock form-inline', 'hidden' => 'true')); //, 'hidden' => 'true'
            $result .= html_writer::tag('label', get_string('answer', 'qtype_striking',
                    html_writer::tag('span', $input, array('class' => 'answer'))),
                    array('for' => $inputattributes['id']));
            $result .= html_writer::end_tag('div');
        }

        $this->page->requires->js_call_amd('qtype_striking/question', 'init');

        if ($qa->get_state() == question_state::$invalid) {
            $result .= html_writer::nonempty_tag('div',
                    $question->get_validation_error(array('answer' => $currentanswer)),
                    array('class' => 'validationerror'));
        }

        return $result;
    }

    public function specific_feedback(question_attempt $qa) {
        $question = $qa->get_question();

        $answer = $question->get_matching_answer(array('answer' => $qa->get_last_qt_var('answer')));
        if (!$answer || !$answer->feedback) {
            return '';
        }

        return $question->format_text($answer->feedback, $answer->feedbackformat,
                $qa, 'question', 'answerfeedback', $answer->id);
    }

    public function correct_response(question_attempt $qa) {
        $question = $qa->get_question();

        //$answer = $question->get_matching_answer($question->get_correct_response());
        $answers = $question->answers;
        $answer="";
        foreach ($answers as $an){
            if($answer!=""){
                $answer .=",";
            }
            $answer .=$an->answer;

        }
        //if (!$answer) {
          //  return '';
        //}

        return get_string('correctansweris', 'qtype_striking',
                $answer);
    }
}
